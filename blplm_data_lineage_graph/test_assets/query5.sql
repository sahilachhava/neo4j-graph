SELECT
L.A_SERIENUMBER,
L.B_MAJORCOMPONENTNUMBER,
L.CREATEDATE,
L.UPDATEDATE,
L.CREATEDATEONPSSSI,
L.UPDATEDATEONPSSSI,
CAST(get_program() AS VARCHAR2(10)) AS PROGRAM,
L.TEAM_O_SERIES,
L.TEAM_O_MAJORCOMPONENT,
L.INTERNALISSUE_O_SERIES,
L.INTERNALISSUE_O_MAJORCOMPONENT,
L.IS_MANAGED_BY_BOM,
L.IS_MANAGED_BY_CAD,
L.IS_MANAGED_BY_CADXML,
L.LINKTYPE,
L.IDA2A2_LNK,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,]+', 1, 1)) AS ROTATION1_0,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,,]+', 1, 2)) AS ROTATION2_0,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,,,)]+', 1, 3)) AS ROTATION3_0,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,,,)T(,]+', 1, 4)) AS TRANSLATION_0,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,,,)T(,,]+', 1, 5)) AS TRANSLATION_1,
TO_NUMBER(REGEXP_SUBSTR (P.MATRIX_ROTATION_TRANSLATION, '[^R(,,,)T(,,)]+', 1, 6)) AS TRANSLATION_2,
CAST(NULL as NUMBER) AS ROTATION1_1,
CAST(NULL as NUMBER) AS ROTATION1_2,
CAST(NULL as NUMBER) AS ROTATION2_1,
CAST(NULL as NUMBER) AS ROTATION2_2,
CAST(NULL as NUMBER) AS ROTATION3_1,
CAST(NULL as NUMBER) AS ROTATION3_2,
L.ROWID AS L_ROWID,
P.ROWID AS P_ROWID
FROM L_SERIE_MAJORCOMPONENT L,
CDC_1U08_N.L_SERIES_MAJORCOMPONENT P
WHERE L.B_MAJORCOMPONENTNUMBER = P.B_LINKED_MAJORCOMPONENT
AND L.A_SERIENUMBER = P.A_LINKED_SERIES