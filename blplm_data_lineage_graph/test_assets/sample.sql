SELECT
    t1.COLUMNT11,
    t1.NLGEO * 100 + t1.NPGECN AS COLUMN2_ALIAS,
    CAST(100 AS INT) AS VAL
FROM DB.TABLE1 t1, DB.TABLE2 t2
WHERE t1.COLUMN1 = 'VALUE1' AND t2.COLUMNT22 = 'VALUE3'
UNION ALL
SELECT
    t2.COLUMNT21 AS COLUMN1_ALIAS,
    t2.COLUMNT22 AS COLUMN2_ALIAS
FROM DB.TABLE2 t2
WHERE t2.COLUMNT21 = 'VALUE2' AND t2.COLUMNT22 = 'VALUE3'
GROUP BY t2.COLUMNT21